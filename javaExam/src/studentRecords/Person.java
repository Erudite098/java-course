package studentRecords;

public class Person {
	
	/* Member variables */
	private String name;
	private int age;
	
	/* Construtor */
	public Person(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	/* Getters */
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}

}
